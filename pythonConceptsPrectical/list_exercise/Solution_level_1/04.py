# 4. Get the first item, the middle item and the last item of the list

li = [10, 20, 30, 40, 50]

first_item = li[0]


lst_length = len(li)
middle_index = lst_length // 2
middle_item = li[middle_index]


last_item = li[-1]

print("First item:", first_item)
print("Middle item:", middle_item)
print("Last item:", last_item)